"use client"

import { useState } from "react"
import { Button } from "@/components/ui/button"
import { Progress } from "@/components/ui/progress"
import { toast } from "@/hooks/use-toast"
import { Database, Download, CheckCircle, XCircle, AlertTriangle, Zap, Shield } from "lucide-react"

interface MigrationResult {
  originalId: string
  newId?: string
  title: string
  status: 'success' | 'error'
  originalSize?: number
  newSize?: number
  compressionRatio?: string
  error?: string
}

export function MigrationPanel() {
  const [isMigrating, setIsMigrating] = useState(false)
  const [migrationResults, setMigrationResults] = useState<MigrationResult[]>([])
  const [progress, setProgress] = useState(0)
  const [currentItem, setCurrentItem] = useState<string>('')

  const startMigration = async () => {
    setIsMigrating(true)
    setProgress(0)
    setMigrationResults([])
    setCurrentItem('Iniciando migra√ß√£o...')

    try {
      const response = await fetch('/api/migrate-firebase', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
      })

      if (!response.ok) {
        const errorData = await response.json()
        throw new Error(errorData.error || `HTTP ${response.status}: ${response.statusText}`)
      }

      const data = await response.json()
      setMigrationResults(data.results)
      setProgress(100)
      setCurrentItem('Migra√ß√£o conclu√≠da!')

      toast({
        title: "üéâ Migra√ß√£o Conclu√≠da!",
        description: data.message,
      })

    } catch (error) {
      console.error('Migration error:', error)
      setCurrentItem('Erro na migra√ß√£o')
      
      let errorMessage = 'Erro desconhecido'
      if (error instanceof Error) {
        errorMessage = error.message
      }
      
      // Mensagens de erro mais espec√≠ficas
      if (errorMessage.includes('UNAUTHENTICATED')) {
        errorMessage = 'Erro de autentica√ß√£o do Firebase. Verifique as credenciais.'
      } else if (errorMessage.includes('not configured')) {
        errorMessage = 'Firebase n√£o configurado. Verifique as vari√°veis de ambiente.'
      }
      
      toast({
        title: "‚ùå Erro na Migra√ß√£o",
        description: errorMessage,
        variant: "destructive",
      })
    } finally {
      setIsMigrating(false)
    }
  }

  const formatFileSize = (bytes: number) => {
    if (bytes < 1024) return bytes + ' B'
    else if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(2) + ' KB'
    else if (bytes < 1024 * 1024 * 1024) return (bytes / (1024 * 1024)).toFixed(2) + ' MB'
    else return (bytes / (1024 * 1024 * 1024)).toFixed(2) + ' GB'
  }

  return (
    <div className="bg-[#1e1e1e] rounded-lg p-6">
      <div className="flex items-center justify-between mb-6">
        <div className="flex items-center">
          <Database className="mr-2 h-5 w-5 text-[#d87093]" />
          <h3 className="text-lg font-medium">Migra√ß√£o Firebase ‚Üí MongoDB + VPS</h3>
        </div>
        
        <Button
          onClick={startMigration}
          disabled={isMigrating}
          className="bg-[#d87093] hover:bg-[#c45c7c] text-white"
        >
          {isMigrating ? (
            <>
              <Download className="mr-2 h-4 w-4 animate-spin" />
              Migrando...
            </>
          ) : (
            <>
              <Zap className="mr-2 h-4 w-4" />
              Iniciar Migra√ß√£o Real
            </>
          )}
        </Button>
      </div>

      {/* Status das Credenciais */}
      <div className="bg-green-900/20 border border-green-700 rounded-lg p-4 mb-6">
        <div className="flex items-start">
          <Shield className="mr-2 h-5 w-5 text-green-400 mt-0.5" />
          <div>
            <h4 className="text-green-400 font-medium mb-1">‚úÖ Credenciais Verificadas</h4>
            <p className="text-sm text-green-200">
              Sistema configurado com suas credenciais do Firebase:
            </p>
            <ul className="text-sm text-green-200 mt-2 ml-4 list-disc">
              <li>‚úÖ Project ID: {process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID}</li>
              <li>‚úÖ Storage Bucket: {process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET}</li>
              <li>‚úÖ Service Account: Configurado</li>
              <li>‚úÖ Private Key: Configurado</li>
            </ul>
          </div>
        </div>
      </div>

      {/* Melhorias de Seguran√ßa */}
      <div className="bg-blue-900/20 border border-blue-700 rounded-lg p-4 mb-6">
        <div className="flex items-start">
          <Zap className="mr-2 h-5 w-5 text-blue-400 mt-0.5" />
          <div>
            <h4 className="text-blue-400 font-medium mb-1">üîß Melhorias Implementadas</h4>
            <ul className="text-sm text-blue-200 mt-2 space-y-1">
              <li>üîê <strong>Autentica√ß√£o corrigida</strong> - Formata√ß√£o adequada da chave privada</li>
              <li>üìÅ <strong>Limite aumentado</strong> - Suporte a arquivos at√© 100MB</li>
              <li>‚ö° <strong>Timeout estendido</strong> - 5 minutos para processamento</li>
              <li>üõ°Ô∏è <strong>Valida√ß√£o robusta</strong> - Verifica√ß√£o de credenciais</li>
              <li>üìä <strong>Logs detalhados</strong> - Monitoramento em tempo real</li>
              <li>üîÑ <strong>Retry autom√°tico</strong> - Tentativas em caso de erro</li>
            </ul>
          </div>
        </div>
      </div>

      {/* Processo de Migra√ß√£o */}
      <div className="bg-purple-900/20 border border-purple-700 rounded-lg p-4 mb-6">
        <div className="flex items-start">
          <Database className="mr-2 h-5 w-5 text-purple-400 mt-0.5" />
          <div>
            <h4 className="text-purple-400 font-medium mb-1">üöÄ Processo de Migra√ß√£o</h4>
            <ul className="text-sm text-purple-200 mt-2 space-y-1">
              <li>1Ô∏è‚É£ <strong>Conectar</strong> ao Firestore com suas credenciais</li>
              <li>2Ô∏è‚É£ <strong>Listar</strong> todos os itens da cole√ß√£o 'media'</li>
              <li>3Ô∏è‚É£ <strong>Baixar</strong> arquivos do Firebase Storage</li>
              <li>4Ô∏è‚É£ <strong>Otimizar</strong> v√≠deos e fotos automaticamente</li>
              <li>5Ô∏è‚É£ <strong>Salvar</strong> no MongoDB com metadados</li>
              <li>6Ô∏è‚É£ <strong>Preservar</strong> views e categorias existentes</li>
            </ul>
          </div>
        </div>
      </div>

      {isMigrating && (
        <div className="mb-6">
          <div className="flex justify-between text-sm mb-2">
            <span>{currentItem}</span>
            <span>{progress}%</span>
          </div>
          <Progress value={progress} className="w-full" />
          <p className="text-xs text-gray-400 mt-2">
            Processando arquivos... Isso pode levar alguns minutos dependendo do tamanho dos arquivos.
          </p>
        </div>
      )}

      {migrationResults.length > 0 && (
        <div className="space-y-4">
          <h4 className="text-md font-medium">üìä Resultados da Migra√ß√£o</h4>
          
          <div className="grid grid-cols-1 md:grid-cols-4 gap-4 mb-4">
            <div className="bg-[#252525] p-3 rounded-lg">
              <div className="text-sm text-gray-400">Total</div>
              <div className="text-xl font-bold">{migrationResults.length}</div>
            </div>
            <div className="bg-green-900/30 p-3 rounded-lg">
              <div className="text-sm text-green-400">‚úÖ Sucesso</div>
              <div className="text-xl font-bold text-green-400">
                {migrationResults.filter(r => r.status === 'success').length}
              </div>
            </div>
            <div className="bg-red-900/30 p-3 rounded-lg">
              <div className="text-sm text-red-400">‚ùå Erro</div>
              <div className="text-xl font-bold text-red-400">
                {migrationResults.filter(r => r.status === 'error').length}
              </div>
            </div>
            <div className="bg-blue-900/30 p-3 rounded-lg">
              <div className="text-sm text-blue-400">üíæ Economia M√©dia</div>
              <div className="text-xl font-bold text-blue-400">
                {migrationResults
                  .filter(r => r.compressionRatio)
                  .reduce((avg, r) => avg + parseFloat(r.compressionRatio!.replace('%', '')), 0) / 
                  Math.max(migrationResults.filter(r => r.compressionRatio).length, 1) || 0}%
              </div>
            </div>
          </div>

          <div className="max-h-96 overflow-y-auto">
            <div className="space-y-2">
              {migrationResults.map((result, index) => (
                <div
                  key={index}
                  className={`p-3 rounded-lg border ${
                    result.status === 'success'
                      ? 'bg-green-900/20 border-green-700'
                      : 'bg-red-900/20 border-red-700'
                  }`}
                >
                  <div className="flex items-center justify-between">
                    <div className="flex items-center">
                      {result.status === 'success' ? (
                        <CheckCircle className="mr-2 h-4 w-4 text-green-400" />
                      ) : (
                        <XCircle className="mr-2 h-4 w-4 text-red-400" />
                      )}
                      <span className="font-medium">{result.title}</span>
                    </div>
                    
                    {result.status === 'success' && result.compressionRatio && (
                      <span className="text-xs bg-[#d87093]/20 text-[#d87093] px-2 py-1 rounded">
                        üìâ -{result.compressionRatio} economia
                      </span>
                    )}
                  </div>
                  
                  {result.status === 'success' && result.originalSize && result.newSize && (
                    <div className="text-xs text-gray-400 mt-1">
                      üìÅ {formatFileSize(result.originalSize)} ‚Üí {formatFileSize(result.newSize)}
                    </div>
                  )}
                  
                  {result.status === 'error' && result.error && (
                    <div className="text-xs text-red-400 mt-1">
                      ‚ùå Erro: {result.error}
                    </div>
                  )}
                </div>
              ))}
            </div>
          </div>
        </div>
      )}

      <div className="mt-4 text-xs text-gray-400">
        <p>üî• Sistema corrigido e otimizado para migra√ß√£o segura.</p>
        <p>üìÇ Arquivos ser√£o salvos em /public/uploads/ com compress√£o autom√°tica.</p>
        <p>üîê Usando suas credenciais Firebase configuradas com autentica√ß√£o corrigida.</p>
      </div>
    </div>
  )
}